  let startX = 0;
  let endX = 0;

  matchesSlider.addEventListener('touchstart', e => {
    startX = e.touches[0].clientX;
  });

  matchesSlider.addEventListener('touchend', e => {

    endX = e.changedTouches[0].clientX;

    handleSwipe();

  });

  function handleSwipe(){

    const activeIndex =
      groups.findIndex(group =>
        group === currentGroup.innerText
      );

    if(startX - endX > 50){

      if(activeIndex < groups.length - 1){
        switchGroup(activeIndex + 1);
      }

    } else if(endX - startX > 50){

      if(activeIndex > 0){
        switchGroup(activeIndex - 1);
      }

    }

  }

